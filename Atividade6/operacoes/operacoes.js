let nota1 = parseFloat(prompt("Digite o número 1: "));
let nota2 = parseFloat(prompt("Digite o número 2: "));

alert("Soma: " + (nota1 + nota2) + "\n" +
    "Subtração: " + (nota1 - nota2) + "\n" +
    "Produto: " + (nota1 * nota2) + "\n" +
    "Divisão: " + (nota1 / nota2) + "\n" +
    "Resto: " + (nota1 % nota2));